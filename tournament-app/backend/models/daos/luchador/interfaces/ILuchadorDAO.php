<?php

/*
|--------------------------------------------------------------------------
| INTERFACE LUCHADOR DAO
|--------------------------------------------------------------------------
| Budokai Martial Arts Tournament System
|--------------------------------------------------------------------------
| Contrato principal del módulo LUCHADORES
|--------------------------------------------------------------------------
*/

interface ILuchadorDAO {

    /*
    |--------------------------------------------------------------------------
    | Obtener todos los luchadores
    |--------------------------------------------------------------------------
    */

    public function getAll();

    /*
    |--------------------------------------------------------------------------
    | Obtener luchador por ID
    |--------------------------------------------------------------------------
    */

    public function getById($id);

    /*
    |--------------------------------------------------------------------------
    | Crear luchador
    |--------------------------------------------------------------------------
    */

    public function create($data);

    /*
    |--------------------------------------------------------------------------
    | Actualizar luchador
    |--------------------------------------------------------------------------
    */

    public function update($id, $data);

    /*
    |--------------------------------------------------------------------------
    | Eliminar luchador
    |--------------------------------------------------------------------------
    */

    public function delete($id);

    /*
    |--------------------------------------------------------------------------
    | Buscar luchador
    |--------------------------------------------------------------------------
    */

    public function search($search);

    /*
    |--------------------------------------------------------------------------
    | Obtener por categoría
    |--------------------------------------------------------------------------
    */

    public function getByCategoria($categoria);

    /*
    |--------------------------------------------------------------------------
    | Obtener activos
    |--------------------------------------------------------------------------
    */

    public function getActivos();

    /*
    |--------------------------------------------------------------------------
    | Contar luchadores
    |--------------------------------------------------------------------------
    */

    public function countAll();

    /*
    |--------------------------------------------------------------------------
    | Contar por categoría
    |--------------------------------------------------------------------------
    */

    public function countByCategoria($categoria);

    /*
    |--------------------------------------------------------------------------
    | Obtener ranking
    |--------------------------------------------------------------------------
    */

    public function getRanking();

    /*
    |--------------------------------------------------------------------------
    | Obtener top victorias
    |--------------------------------------------------------------------------
    */

    public function getTopVictorias();

    /*
    |--------------------------------------------------------------------------
    | Verificar documento existente
    |--------------------------------------------------------------------------
    */

    public function documentoExists($documento);

}
?>